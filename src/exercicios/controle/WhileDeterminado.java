package exercicios.controle;

public class WhileDeterminado { // Determinado por termos utilizado um contador,  que define a quantidade de repetição.

    public static void main(String[] args) {

        int contador = 1;

        while(contador <=10) {
            System.out.printf("i = %d\n", contador);
            contador ++;
        }
    }
}
