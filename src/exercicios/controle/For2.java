package exercicios.controle;

public class For2 {

    public static void main(String[] args) {

        for (int contador = 90; contador >= 0; contador-=2) { // 1° parte: declaração da variável - 2° parte: expressão booleana e 3° parte: manipulacao para finalizar o codigo
            System.out.printf("i = %d\n", contador);
        }
    }
}
