package exercicios.controle;

public class For1 {
    public static void main(String[] args) {

        for (int contador = 1; contador <= 10; contador++) { // 1° parte: declaração da variável - 2° parte: expressão booleana e 3° parte: manipulacao para finalizar o codigo
            System.out.printf("i = %d\n", contador);
        }
    }
}